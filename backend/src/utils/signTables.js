import prisma from './prisma.js';
import { executeRaw, queryRaw } from './dbRaw.js';

let ensured = false;

export function getDatabaseProvider() {
  const directProvider = String(
    process.env.DATABASE_PROVIDER ||
    process.env['\uFEFFDATABASE_PROVIDER'] ||
    ''
  )
    .trim()
    .toLowerCase();

  if (directProvider) {
    return directProvider;
  }

  const candidateUrls = [
    process.env.DATABASE_URL,
    process.env.DATABASE_URL_LOCAL,
    process.env.DATABASE_URL_PRODUCTION,
    process.env.DATABASE_URL_STAGING,
    process.env.DATABASE_URL_TEST,
  ]
    .map((value) => String(value || '').trim().toLowerCase())
    .filter(Boolean);

  if (candidateUrls.some((value) => value.startsWith('postgres://') || value.startsWith('postgresql://'))) {
    return 'postgresql';
  }

  return 'sqlite';
}

export function isPostgresDatabase() {
  return getDatabaseProvider() === 'postgresql';
}

export async function listTableColumns(table) {
  if (isPostgresDatabase()) {
    return queryRaw(
      `
        SELECT column_name
        FROM information_schema.columns
        WHERE table_name = ?
          AND table_schema = current_schema()
      `,
      table,
    );
  }

  return prisma.$queryRawUnsafe(`PRAGMA table_info(${table})`);
}

async function hasColumn(table, column) {
  const rows = await listTableColumns(table);
  const normalizedTarget = String(column || '').trim().toLowerCase();
  return rows.some((row) =>
    String(row.column_name || row.name || '')
      .trim()
      .toLowerCase() === normalizedTarget
  );
}

async function ensureColumn(table, column, sqliteDefinition, postgresDefinition = sqliteDefinition) {
  if (await hasColumn(table, column)) {
    return;
  }

  const definition = isPostgresDatabase() ? postgresDefinition : sqliteDefinition;
  if (isPostgresDatabase()) {
    await executeRaw(`ALTER TABLE ${table} ADD COLUMN "${column}" ${definition}`);
    return;
  }

  await executeRaw(`ALTER TABLE ${table} ADD COLUMN ${column} ${definition}`);
}

