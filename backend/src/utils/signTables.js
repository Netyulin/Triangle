export async function ensureSignTables() {
  if (ensured) {
    return;
  }

  await ensureBaseTables();

  await ensureColumn('sign_tasks', 'certificateId', 'INTEGER');
  await ensureColumn('sign_tasks', 'profileId', 'INTEGER');
  await ensureColumn('sign_tasks', 'expiresAt', 'DATETIME', 'TIMESTAMP');
  await ensureColumn('sign_tasks', 'cleanedAt', 'DATETIME', 'TIMESTAMP');

  await ensureColumn('sign_certificates', 'ownerUserId', 'INTEGER');
  await ensureColumn('sign_certificates', 'scope', "TEXT NOT NULL DEFAULT 'system'");

  await ensureColumn('sign_profiles', 'ownerUserId', 'INTEGER');
  await ensureColumn('sign_profiles', 'scope', "TEXT NOT NULL DEFAULT 'system'");

  ensured = true;
}
